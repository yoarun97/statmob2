function slugify(name: string): string {
  return name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
}

const outputDir = join(process.cwd(), 'src/data/players');
mkdirSync(outputDir, { recursive: true });

const index = PLAYERS.map((p) => {
  const id = slugify(p.name);
  const detail: PlayerDetail = { id, ...p };

  // Write individual player file
  writeFileSync(join(outputDir, `${id}.json`), JSON.stringify(detail, null, 2));

  // Return the index entry (without seasonStats)
  const { seasonStats: _, ...indexEntry } = detail;
  return indexEntry;
});

writeFileSync(join(outputDir, 'index.json'), JSON.stringify(index, null, 2));

